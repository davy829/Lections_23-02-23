void PrintPic(int[,] MyPic)
{
    for (int row = 0; row < MyPic.GetLength(0); row++)
    {
        for (int col = 0; col < MyPic.GetLength(1); col++)
        {
            if (MyPic[row, col] == 0) Console.Write($"  ");
            else Console.Write($" +");
        }
        Console.WriteLine();
    }
}



void FullImage(int row, int col)
{
